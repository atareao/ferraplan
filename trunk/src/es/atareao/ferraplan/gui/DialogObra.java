    private void doClose(int retStatus) {
        if(retStatus==RET_OK){
            try {
                if(this.fromDialog().validate()){
                    this.setObra(this.fromDialog());
                    this.setReturnStatus(retStatus);
                    setVisible(false);
                    dispose();
                }
            } catch (Exception ex) {
                 ErrorDialog.manejaError(ex,false);
            }
        }else{
            this.setReturnStatus(retStatus);
            setVisible(false);
            dispose();
        }
    }    

    private void doNotEditable(){
        this.jContrata.setEditable(false);
        this.jNombre.setEditable(false);
        //
        this.jContrata.setBackground(java.awt.SystemColor.info);
        this.jNombre.setBackground(java.awt.SystemColor.info);
    }
    
    private Obra fromDialog(){
        String contrata=jContrata.getText();
        String nombre=jNombre.getText();
        this.getObra().setValue("CONTRATA",contrata);
        this.getObra().setValue("NOMBRE",nombre);
        return this.getObra();
    }
    private void toDialog(Obra obra){
        String contrata=obra.getValue("CONTRATA");
        String nombre=obra.getValue("NOMBRE");
        this.jContrata.setText(contrata);
        this.jNombre.setText(nombre);
    } 
    
