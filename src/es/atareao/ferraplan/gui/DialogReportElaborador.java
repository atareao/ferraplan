    public DialogReportElaborador(java.awt.Frame parent, boolean modal,CompactDb cdb) {
        super(parent, modal);
        initComponents();
        this.setSize(410, 240);
        this.setLocationRelativeTo(null);
        this.jDesde.setDate(DateUtils.addMonths(DateUtils.Ahora(), -1));
        this.jHasta.setDate(DateUtils.Ahora());
        try {
            Elaborador elaborador = new Elaborador(cdb.getConector());
            this.jElaborador.setElements(elaborador.find("orden"));
        } catch (SQLException ex) {
            ErrorDialog.manejaError(ex);
        }

    }
    private void doClose(int retStatus) {
        if(retStatus==RET_OK){
            this.setDesde(this.jDesde.getDate());
            this.setHasta(this.jHasta.getDate());
        }
        this.setReturnStatus(retStatus);
        setVisible(false);
        dispose();
    }
